package net.enilink.komma.core;

/**
 * This class represents a generic blank node.
 * 
 * Blank nodes with the same nominal value are considered equal.
 * 
 */
public class BlankNode implements IReference {
	private String id;

	public BlankNode() {
		id = "_:komma-" + System.identityHashCode(this);
	}

	public BlankNode(String id) {
		this.id = id;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		BlankNode other = (BlankNode) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

	/**
	 * Returns <code>null</code>, since a blank node does not have an
	 * {@link URI}
	 */
	@Override
	public URI getURI() {
		return null;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public String toString() {
		return id;
	}
}
